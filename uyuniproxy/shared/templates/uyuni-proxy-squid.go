package templates

import (
	"io"
	"text/template"
)

const proxySquidTemplate = `# uyuni-proxy-squid.service, generated by uyuniadm
# Use an uyuni-proxy-squid.service.d/local.conf file to override

[Unit]
Description=Uyuni proxy squid container service
Wants=network.target
After=network-online.target
BindsTo=uyuni-proxy-pod.service
After=uyuni-proxy-pod.service

[Service]
Environment=PODMAN_SYSTEMD_UNIT=%n
EnvironmentFile={{ .ProxySystemdConfigPath }}
EnvironmentFile={{ .ProxyConfigFolder}}
EnvironmentFile=-{{ .ProxySquidConfigPath}}
Restart=on-failure
ExecStartPre=/bin/rm -f %t/uyuni-proxy-squid.pid %t/uyuni-proxy-squid.ctr-id

Environment=UYUNI_IMAGE={{ .Image }}
ExecStart=/usr/bin/podman run \
    --conmon-pidfile %t/{{ .NamePrefix }}-proxy-squid.pid \
    --cidfile %t/{{ .NamePrefix }}-proxy-squid.ctr-id \
    --cgroups=no-conmon \
    --pod-id-file %t/{{ .NamePrefix }}-proxy-pod.pod-id -d \
    --replace -dt \
	  {{- range $name, $path := .Volumes }}
	  -v {{ $name }}:{{ $path }} \
	  {{- end }}
    --name {{ .NamePrefix }}-proxy-squid ${UYUNI_IMAGE}

ExecStop=/usr/bin/podman stop --ignore --cidfile %t/{{ .NamePrefix }}-proxy-squid.ctr-id -t 10
ExecStopPost=/usr/bin/podman rm --ignore -f --cidfile %t/{{ .NamePrefix }}-proxy-squid.ctr-id
PIDFile=%t/{{ .NamePrefix }}-proxy-squid.pid
TimeoutStopSec=60
Type=forking

[Install]
WantedBy=multi-user.target default.target
`

type PodmanProxySquidTemplateData struct {
	Volumes                map[string]string
	ProxySystemdConfigPath string
	ProxyConfigFolder      string
	ProxySquidConfigPath   string
	NamePrefix             string
}

func (data PodmanProxySquidTemplateData) Render(wr io.Writer) error {
	t := template.Must(template.New("service").Parse(proxySquidTemplate))
	return t.Execute(wr, data)
}
